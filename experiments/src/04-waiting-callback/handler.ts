import {
  DurableContext,
  withDurableExecution,
} from "@aws/durable-execution-sdk-js";

export interface WaitCallbackEvent {
  message: string;
}

export interface WaitCallbackResult {
  before: string;
  received: string;
  after: string;
}

/**
 * The function stops completely at the waitForCallback and only continues
 * when someone calls SendDurableExecutionCallbackSuccess with the callbackId.
 */
export const lambdaHandler = withDurableExecution(
  async (
    event: WaitCallbackEvent,
    context: DurableContext,
  ): Promise<WaitCallbackResult> => {
    const before = await context.step("step-before", async () => {
      return `before: ${event.message}`;
    });

    // Function suspends here until the callback is received.
    // The callbackId is generated by the SDK and can be used to resume this execution.
    const received = await context.waitForCallback<string>(
      "wait-for-callback",
      async (callbackId) => {
        context.logger.info(
          "Function suspended. Resume with this callbackId:",
          {
            callbackId,
          },
        );
      },
    );

    // Only runs after the callback is received
    const after = await context.step("step-after", async () => {
      return `after: ${received}`;
    });

    return { before, received, after };
  },
);
